public class ParserHelper {

	public static MobileElement convertTestObjectToMobileElement(TestObject to) {
		String xpath = to.findProperty("xpath").getValue()
		AppiumDriver<MobileElement> driver = MobileDriverFactory.getDriver()
		MobileElement mobileElement = (MobileElement) driver.findElementByXPath(xpath)
		return mobileElement
	}

	public static WebElement converTestObjectToWebElement(TestObject to) {
		return WebUiCommonHelper.findWebElement(to, GlobalVariable.G_min_timeout)
	}
}
